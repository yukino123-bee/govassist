  static Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    currentLanguage.value = prefs.getString('language') ?? 'en';
  }

  static Future<void> setLanguage(String langCode) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('language', langCode);
    currentLanguage.value = langCode;
  }

  static String translate(String key) {
    final lang = currentLanguage.value;
    if (_translations.containsKey(lang) && _translations[lang]!.containsKey(key)) {
      return _translations[lang]![key]!;
    }
    if (_translations['en']!.containsKey(key)) {
      return _translations['en']![key]!;
    }
    return key;
  }

  static void addTranslations(String lang, Map<String, String> translations) {
    if (!_translations.containsKey(lang)) {
      _translations[lang] = {};
    }
    _translations[lang]!.addAll(translations);
  }
}

extension TranslationExtension on String {
  String tr() {
    return AppTranslations.translate(this);
  }
}
